class operations:

    def add(self, num1, num2):
        
        return num1 + num2
    
    def sub(self, num1, num2):
        
        return num1 - num2
    
    def product(self, num1, num2):
        
        return num1 * num2    
    
    def divide(self, num1 ,num2):
        
        if num2 == 0:
            return "Infinty"    
        return num1 / num2 

    def square(self, num1):
        
        return num1**2 
    
    def squareRoot(self,num1):
        
        return num1**0.5

    def power(self, number , power):

        return number ** power
    